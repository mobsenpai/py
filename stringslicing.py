print(var[0]) # H
print(var[0:5:1]) # Hello
print(var[0::1]) # Hello world
print(var[0::]) # Hello world
print(var[::]) # Hello world
print(var[-1]) # d
print(var[-1::]) # d
print(var[-1::-1]) # dlrow olleH
print(var[::-1]) # dlrow olleH
print(var[-1:-5:-1]) # dlro
print(var[-1:5:-1]) # dlrow
print(var[:5:-1]) # dlrow
