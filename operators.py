a = 54
b = 3

# ----------- ( arithmetic operators)
print("a + b = ", a + b); # add
print("a - b = ", a - b); # subtract
print("a * b = ", a * b); # multiply
print("a / b = ", a / b); # divide
print("a % b = ", a % b); # quotient
print("a // b = ", a // b); # floor division

# ----------- ( logical operators)
print("a > b is ", a > b); # greater than
print("a < b is ", a < b); # less than
print("a >= b s ", a >= b); # greater than or equal to
print("a <= b is ", a <= b); # less than or equal to

