# Basic function definitions

# greet user
def greet_user(user):
    print(f"Hello, {user}!")


greet_user("Yash Raj")


# square number
def square_number(num):
    print(f"The square of {num} is {num ** 2}")

square_number(2)


# fav colour
def favourite_color(name, color):
    print(f"{name}'s favourite color is {color}")

favourite_color("Yash", "Green")
